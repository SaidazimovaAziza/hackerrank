def sum_of_numbers(first_number, second_number):
    return first_number+second_number


def product_of_numbers(first_number, second_number):
    return first_number*second_number


def subtraction_of_numbers(first_number, second_number):
    return first_number-second_number


first_number = int(input())
second_number = int(input())
print(sum_of_numbers(first_number, second_number))
print(product_of_numbers(first_number, second_number))
print(subtraction_of_numbers(first_number, second_number))
